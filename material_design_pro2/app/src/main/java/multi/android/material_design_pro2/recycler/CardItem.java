package multi.android.material_design_pro2.recycler;

public class CardItem {

    int cardimg;
    String str;

    public CardItem(int cardimg, String str) {
        this.cardimg = cardimg;
        this.str = str;
    }

    public int getCardimg() {
        return cardimg;
    }

    public String getStr() {
        return str;
    }
}

